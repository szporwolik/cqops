func DeriveBand(freqMHz float64) string {
	for _, r := range bandRanges {
		if freqMHz >= r.low && freqMHz <= r.high {
			return r.name
		}
	}
	return ""
}

type bandRange struct {
	low, high float64
	name      string
}

